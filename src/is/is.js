/**
 * Test if a variable holds something
 *
 * @name is
 *
 * @tag Core
 * @signature is(source): boolean
 *
 * @param {any} source Source variable
 *
 * @return {boolean}
 *
 * @example
 *
 * is(null)      // => false
 * is(0)         // => true
 * is(undefined) // => false
 * is("")        // => true
 * is(false)     // => true
 * is(NaN)       // => false
 */
const is = source =>
  source !== null && source !== undefined && !Number.isNaN(source)

export { is }
